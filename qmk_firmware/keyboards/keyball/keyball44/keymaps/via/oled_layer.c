#define ICON_SIZE 32  // 16x16ピクセル = 32バイト
#define ICONS_PER_LAYER 2  // 各レイヤーのアイコンは2行

void keyball_oled_render_layer_icon(void) {
    const int layer = get_highest_layer(layer_state);
    const int offset = layer * ICON_SIZE;

    // 上半分のアイコンを描画
    oled_set_cursor(2, 9);
    oled_write_raw_P(layer_icons + offset, ICON_SIZE / 2);

    // 下半分のアイコンを描画
    oled_set_cursor(2, 10);
    oled_write_raw_P(layer_icons + offset + (ICON_SIZE / 2), ICON_SIZE / 2);
}